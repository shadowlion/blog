<template lang="pug">
  button.toggle-theme(role="button" aria-label="Toggle dark/light" @click.prevent="toggleTheme")
    sun-icon(v-if="darkTheme" size="24")
    moon-icon(v-else size="24")
  </button>
</template>

<script>
import { SunIcon, MoonIcon } from 'vue-feather-icons'

export default {
  components: {
    SunIcon, MoonIcon
  },
  data() {
    return {
      darkTheme: true
    }
  },
  methods: {
    toggleTheme() {
      this.darkTheme = !this.darkTheme

      // This is using a script that is added in index.html
      window.__setPreferredTheme(
        this.darkTheme ? 'dark' : 'light'
      )
    }
  },
  mounted() {
    if (window.__theme == 'dark') this.darkTheme = true
  }
}
</script>

<style lang="scss">
.toggle-theme {
  background-color: transparent;
  border: 0;
  color: var(--body-color);
  cursor: pointer;

  &:hover {
    opacity: .8
  }
  &:focus {
    outline: none;
  }
}
</style>
